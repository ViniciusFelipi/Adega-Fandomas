    private void tblBebidasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBebidasMouseClicked
        // TODO add your handling code here:
                // Captura o índice da linha que o usuário clicou
        int linhaSelecionada = tblBebidas.getSelectedRow();

        // Verificação de segurança: se o índice for -1, nada foi selecionado
        if (linhaSelecionada != -1) {

            // Captura os valores da JTable e joga nos campos de texto
            // O método getValueAt(linha, coluna) retorna um Object, por isso o .toString()
            // Coluna 0: ID
            txtId.setText(tblBebidas.getValueAt(linhaSelecionada, 0).toString());

            // Coluna 1: Nome
            txtNome.setText(tblBebidas.getValueAt(linhaSelecionada, 1).toString());

            // Coluna 2: Descrição
            txtDescricao.setText(tblBebidas.getValueAt(linhaSelecionada, 2).toString());

            // Coluna 3: Preço de Venda
            // Dica: Se quiser formatar para aparecer com "R$", você pode tratar aqui
            txtPreco.setText(tblBebidas.getValueAt(linhaSelecionada, 3).toString());

            // Opcional: Desabilitar o campo ID se ele for auto-incremento no banco
            txtId.setEditable(false);
        }

    }//GEN-LAST:event_tblBebidasMouseClicked

