 public void listarBebidas(String nomeBusca) {
        DefaultTableModel modelo = (DefaultTableModel) tblBebidas.getModel();
        modelo.setNumRows(0);

        // Instanciar a DAO para usar o método
        BebidasDAO dao = new BebidasDAO();

        // Chamar o método através da instância 'dao'
        for (Bebidas b : dao.pesquisarBebidas(nomeBusca)) {

            // Formatar para 2 casas decimais
            String precoFormatado = String.format("%.2f", b.getPrecoVenda());

            modelo.addRow(new Object[]{
                b.getId(),
                b.getNome(),
                b.getDescricao(),
                "R$ " + precoFormatado // 3. Usa a String bonitona aqui!
            });
        }
    }


